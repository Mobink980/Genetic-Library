<script>
$(document).ready(function(){
 
 $('#comment_form').on('submit', function(event){
  event.preventDefault();
  var form_data = $(this).serialize();//creat text string in standard URL
  $.ajax({
   url:"add_comment.php",
   method:"POST",
   data:form_data,
   dataType:"JSON",
   success:function(data)
   {
    if(data.error != '')
    {
     $('#comment_form')[0].reset(); //Syntax to convert jQuery element to a JavaScript object.because jQ dosen't have reset()
     $('#comment_message').html(data.error);//sets the content (innerHTML) of the selected elements.
     $('#comment_id').val('0');//The val() methodsets the value attribute of the selected elements.
     load_comment();//show comment that user sends
    }
   }
  })
 });

 load_comment();//show all comments

 function load_comment()
 {
  $.ajax({
   url:"fetch_comment.php",
   method:"POST",
   success:function(data)
   {
    $('#display_comment').html(data);
   }
  })
 }

 $(document).on('click', '.reply', function(){
  var comment_id = $(this).attr("id");
  $('#comment_id').val(comment_id);
  $('#comment_name').focus(); //The focus event occurs when an element gets focus
 });
 
});
</script>
